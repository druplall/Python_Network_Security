# The order in which the computer store data is very important.
## A byte is 8 bits
## A bit is a single character
# Example: 
# First byte represent 1000
# Second byte represent 200
# Third byte represent 4
## The order in which this example is writen is the biggest value will be written first,
# this is known as Big Endian (AMD does the opposite, little Endian)
# Network Byte order uses the Big Endian method.